void main() {
  String? str;
  bool boolVariable = true;
  int intVariable = 123;
  double doubleVariable = 45.68;

  // boolean to string
  str = boolVariable.toString();
  print(str);

  // integer to string
  str = intVariable.toString();
  print(str);

  // double to string
  str = doubleVariable.toString();
  print(str);
}
