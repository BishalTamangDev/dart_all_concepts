// relational operators :: ==, !=, <, >, <=, >=
void main() {
  int var1 = 10;
  int var2 = 20;

  // comparing the strings
  if (var1 == var2) {
    print("They are equal.");
  } else if (var1 > var2) {
    print("var1($var1) is greater than var2($var2).");
  } else {
    print("var2($var2) is greater than var1($var1).");
  }
}
